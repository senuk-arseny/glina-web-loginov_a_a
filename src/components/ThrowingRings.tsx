// Сигнатурный элемент: концентрические кольца, как следы рёбра инструмента на
// вращающемся сосуде. В хиро — акцент на переднем плане, здесь — используется
// повторно как еле заметный фон, чтобы разреженные экраны (профиль, пустые списки)
// не выглядели как случайно оборванная вёрстка.
export function ThrowingRings({ className }: { className?: string }) {
  return (
    <svg className={`throwing-rings ${className ?? ""}`} viewBox="0 0 400 400" aria-hidden="true">
      <circle cx="200" cy="200" r="180" />
      <circle cx="200" cy="200" r="150" />
      <circle cx="200" cy="200" r="118" />
      <circle cx="200" cy="200" r="84" />
      <circle cx="200" cy="200" r="48" />
    </svg>
  );
}
